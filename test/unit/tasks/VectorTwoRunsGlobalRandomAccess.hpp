template<class ARCH>
struct VectorTwoRunsGlobalRandomAccess : bpl::Task<ARCH>
{
    USING(ARCH);

    auto operator() (global<vector_view<uint32_t>>  input) const
    {
        return (*input)[0];
    }

    static auto reduce (uint64_t a, uint64_t b) { return a+b; }
};
