template<class ARCH>
struct Parrot5
{
    USING(ARCH);

    auto operator() (const  array<uint8_t,1024>& data)
    {
        uint64_t sum = 0;  for (auto c : data)  {  sum += c;  }  return sum;
    }

    static auto reduce (uint64_t a, uint64_t b)  { return a+b; }
};
