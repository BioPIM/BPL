template<class ARCH>
struct VectorFlush
{
    USING(ARCH);

    auto operator() (size_t n) const
    {
        vector<uint32_t> result;

        for (size_t i=0; i<n; i++)
        {
            result.push_back (i);
        }

        return result;
    }
};
