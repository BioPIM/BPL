template<class ARCH>
struct Vector1
{
    USING(ARCH);

    using vector_t = vector<uint32_t>;

    uint32_t operator() (int nbitems)
    {
        vector_t v;

        for (int i=1; i<=nbitems; i++)  { v.push_back (i); }

        return v.size();
    }
};
