template<class ARCH>
struct SplitRangeInt
{
    USING(ARCH);

    auto operator() (Range range)
    {
        return accumulate (range, uint64_t(0));
    }

    static auto reduce (uint64_t a, uint64_t b)  { return a+b; }
};
