template<class ARCH>
struct Exception1 : bpl::Task<ARCH>
{
    USING(ARCH);

    auto operator() (uint16_t n) const
    {
        vector<uint32_t> v;

        // we make an error happen for a given process unit.
        if (this->tuid() == n)  {  v.at(3) = 5;  }

        return 0;
    }
};
