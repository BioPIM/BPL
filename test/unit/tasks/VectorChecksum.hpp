template<class ARCH>
struct VectorChecksum : bpl::core::Task<ARCH>
{
    USING(ARCH);

    auto operator() (const vector<uint32_t>& v)
    {
        uint64_t checksum = 0;

        for (auto x : v)
        {
           checksum += x;
        }

        return checksum;
    }

    static uint64_t reduce (uint64_t a, uint64_t b)  { return a+b; }
};
