template<class ARCH>
struct VectorViewRandomAccess1 : bpl::Task<ARCH>
{
    USING(ARCH);

    auto operator() (vector_view<uint32_t> const& v) const
    {
        uint64_t result = 0;

        for (size_t i=0; i<v.size(); i++)  {  result += v[i];  }

        return result;
    }
};
