template<class ARCH>
struct VectorInputOutput
{
    USING(ARCH);

    using vector_t = vector<uint32_t>;

    auto operator() (const vector_t& v) -> vector_t
    {
        vector_t result;

        for (uint32_t x : v)   {  result.push_back (2*x);  }

        return result;
    }
};
